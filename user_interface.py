"""User Interface Module"""

class UserInterface():
    def welcome_prompt(self):
        """Welcomes the user"""
        print("Welcome to the Quizlet to Anki automation program!")
        print("Please paste your link now.")

    def prompt():
        """Asks user if they want to paste another prompt"""
        print("Would you like to add another link? (y/n)")

    def arrow(self):
        """Arrow used for prompting."""
        input(f"> ", end='')

    def placement_prompt(self):
        """Requests location of textbox"""
        print("Please put mouse over where you would paste the link.")